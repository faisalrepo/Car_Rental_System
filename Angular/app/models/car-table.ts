export class CarTable {
    serial_no:any;
    car_name:any;
    car_number:any;
    car_color:any;
    car_seating:any;
    rent_per_day:any;
    quantity:any;
    total:any;
    constructor(serial_no:any, car_name:any,car_number:any,car_color:any,car_seating:any,rent_per_day:any, quantity:any, total:any){
            this.serial_no=serial_no;
            this.car_name=car_name;
            this.car_number=car_number;
            this.car_color=car_color;
            this.car_seating=car_seating;
            this.rent_per_day=rent_per_day;
            this.quantity = quantity;
            this.total= total;

    }


}
