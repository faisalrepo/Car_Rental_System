export class User {
  a_number:any;
  name:any;
  gender:any;
  email:any;
  password:any;
  phone:any;
  balance:any;

  constructor( a_number:any, name:any, gender:any, email:any, password:any, phone:any, balance:any){
    this.a_number=a_number;
    this.name=name;
    this.gender=gender;
    this.email=email;
    this.password=password;
    this.phone=phone;
    this.balance=balance;
  }
  
}
